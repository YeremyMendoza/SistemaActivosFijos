CREATE TABLE USUARIO (
  USUARIO_NO VARCHAR(45) NOT NULL,
  CI VARCHAR(20) NULL,
  NOMBRE VARCHAR(50) NULL,
  PATERNO VARCHAR(50) NULL,
  MATERNO VARCHAR(50) NULL,
  CARGO VARCHAR(100) NULL,
  PROFESION VARCHAR(100) NULL,
  USUARIO VARCHAR(45) NULL,
  PASSWORD VARBINARY(8000) NULL,
  PRIMARY KEY (USUARIO_NO))


-- -----------------------------------------------------
-- Table `mydb`.`PARTIDA`
-- -----------------------------------------------------
CREATE TABLE PARTIDA (
  PARTIDA_NO VARCHAR(45) NOT NULL,
  NOMBRE VARCHAR(150) NULL,
  PARTIDA INT NULL,
  VIDA_UTIL DECIMAL(10,2) NULL,
  COEFICIENTE DECIMAL(10,2) NULL,
  DEPRECIACION VARCHAR(45) NULL,
  ACTUALIZA VARCHAR(45) NULL,
  USUARIO_USUARIO_NO VARCHAR(45) NOT NULL,
  PRIMARY KEY (PARTIDA_NO),
  INDEX fk_PARTIDA_USUARIO1_idx (USUARIO_USUARIO_NO ASC),
  CONSTRAINT fk_PARTIDA_USUARIO1
    FOREIGN KEY (USUARIO_USUARIO_NO)
    REFERENCES USUARIO (USUARIO_NO))


-- -----------------------------------------------------
-- Table `mydb`.`OFICINA`
