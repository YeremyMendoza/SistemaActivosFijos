  CONSTRAINT fk_INVENTARIO_PARTIDA1
    FOREIGN KEY (PARTIDA_PARTIDA_NO)
    REFERENCES PARTIDA (PARTIDA_NO),
  CONSTRAINT fk_INVENTARIO_EMPLEADO1
    FOREIGN KEY (EMPLEADO_EMP_NO)
    REFERENCES EMPLEADO (EMP_NO),
  CONSTRAINT fk_INVENTARIO_OFICINA1
    FOREIGN KEY (OFICINA_OFICINA_NO)
    REFERENCES OFICINA (OFICINA_NO))


